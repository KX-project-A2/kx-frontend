function make(
  id: string,
  type: MediaType,
  key: keyof typeof IMG,
  prompt: string,
  creator: Creator,
  likes: number,
  aspect: number,
  opts: Partial<Artwork> = {}
): Artwork {
  return {
    id,
    type,
    url: u(IMG[key], 1400),
    thumb: u(IMG[key], 720),
    prompt,
    creator,
    likes,
    liked: false,
    model: type === 'image' ? '제미나이' : 'seedance 2.0',
    quality: type === 'image' ? '4K' : '고화질 1920×1080',
    ratio: aspect >= 1 ? '16:9 · 1376×768' : '1:1 · 1024×1024',
    createdAt: '2026.07.05',
    aspect,
    ...opts,
  };
}

